// deterministicRandom is a fake random source that returns a fixed sequence
// This allows us to test RSA encryption deterministically
type deterministicRandom struct {
	data []byte
	pos  int
}

func (d *deterministicRandom) Read(p []byte) (n int, err error) {
	n = copy(p, d.data[d.pos:])
	d.pos += n
	// If we run out of data, loop back
	if d.pos >= len(d.data) {
		d.pos = 0
	}
	return n, nil
}

